function subEstoque()
{
    const sublateral = document.querySelector("#sub1");
    if(sublateral.style.display == "none" || sublateral.style.display == "")
    {
        sublateral.style.display = "grid";
    }
    else
    {
        sublateral.style.display = "none";
    }
}

